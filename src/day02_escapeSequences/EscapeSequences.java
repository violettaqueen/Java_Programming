package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Students\nToday we have Java  class"); // \n sequence adds a new line
        System.out.println("------------------------------------------------");
        System.out.println("\tJava Programming Language"); // \t adds additional paragraph space
        System.out.println("\t\t\tJava course");

        System.out.println("\\");

        System.out.println("My favorite TV shoe is \"Mazhor\"");



    }


}
   /*
  1. \n: New Line
  2. \t: Paragraph space
  3. \\: Single Back Slash
  4. \": Double quote

    */