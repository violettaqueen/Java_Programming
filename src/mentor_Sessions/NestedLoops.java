package mentor_Sessions;

public class NestedLoops {

    public static void main(String[] args) {

        System.out.println("Violetta");

        for (int i = 1; i <= 5 ; i++) {
            for (int j = 1; j <= i ; j++) {
                System.out.print("* ");
            }
            System.out.println();

        }

    }

}
